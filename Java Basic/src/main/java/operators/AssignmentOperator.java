package operators;

public class AssignmentOperator {
    public static void main(String[] args) {
        // Examples
        int q = 10;

        // Complex assigments
        System.out.println(q += 2);
        System.out.println(q -= 2);
        System.out.println(q /= 2);
        System.out.println(q *= 2);
        System.out.println(q %= 2);

    }
}
